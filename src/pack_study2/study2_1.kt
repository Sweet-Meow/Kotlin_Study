package pack_study2

//val과 var 변수 선언 및 할당하기

fun main() {
    val number = 27 // number변수는 int형으로 추론
    var language = "Korean"
    val secondNumber : Int = 26
    language = "Japanese"

    println("Number : $number")
    println("language : $language")
    println("secondNumber : $secondNumber")

}